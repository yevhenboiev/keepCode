package org.keepcode.security;

import lombok.Data;
import org.keepcode.entity.Subscriber;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

@Data
public class SecurityUser implements UserDetails {

    private String email;
    private String password;
    private List<SimpleGrantedAuthority> authorities;
    private boolean isActive = true;

    public static UserDetails clientToUserDetails(Subscriber subscriber) {
        return new User(
                subscriber.getEmail(), subscriber.getPassword(),
                true,
                true,
                true,
                true,
                subscriber.getRole().getAuthorities());
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return email;
    }

    @Override
    public boolean isAccountNonExpired() {
        return isActive;
    }

    @Override
    public boolean isAccountNonLocked() {
        return isActive;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return isActive;
    }

    @Override
    public boolean isEnabled() {
        return isActive;
    }
}
